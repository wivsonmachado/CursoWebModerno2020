/*
Os operadores relacionais sempre vão retornar verdadeiro
ou falso.

1 igual '=' é um operador de atribuição
2 iguais '==' é um operador relacional, ou seja, compara
um valor com o outro, o sentido literal do token(igual)
3 iguais '===' além de comparar o valor, compara também o
tipo do dado

exclamação igual '!=' compara se os dados são diferentes,
igorando o tipo do dado

exclamação igual igual '!==' compara se o dado e o tipo são
diferentes.

*/



console.log('1)', '1' == 1) // O dado é igual?
console.log('2)', '1' === 1) // O dado e o tipo são iguais?
console.log('3)', '3' != 3) // O dado é diferente?
console.log('4)', '3' !== 3) // O dado e o tipo são é diferentes?
console.log('5)', 3 < 2) // operando1 é menor que operando2?
console.log('6)', 3 > 2) // operando1 é maior que operando2?
console.log('7)', 3 <= 2) // operando1 é menor ou igual que operando2?
console.log('8)', 3 >= 2) // operando1 é maior ou igual que operando2?